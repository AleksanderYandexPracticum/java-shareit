-- Таблица с бронированием
CREATE TABLE IF NOT EXISTS bookings (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, --уникальный идентификатор бронирования
    start_date  TIMESTAMP         NOT NULL,   --дата и время начала бронирования
    end_date    TIMESTAMP         NOT NULL,   --дата и время конца бронирования
    item_id     BIGINT            NOT NULL CONSTRAINT bookings_items_fkey REFERENCES items ON UPDATE CASCADE,--ON DELETE CASCADE ON UPDATE CASCADE,   --вещь, которую пользователь бронирует
    booker_id   BIGINT            NOT NULL CONSTRAINT bookings_users_fkey REFERENCES users ON UPDATE CASCADE,--ON DELETE CASCADE ON UPDATE CASCADE,   --пользователь, который осуществляет бронирование
    status      VARCHAR(20)       NOT NULL --REFERENCES status (id)  --статус бронирования
);

-- Таблица с комментариями
CREATE TABLE IF NOT EXISTS comments (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, --уникальный идентификатор комментария
    text        VARCHAR(10000) NOT NULL,                         --текст комментария
    item_id     BIGINT         NOT NULL    REFERENCES items (id),  --вещь, на которую комментарий
    author_id   BIGINT         NOT NULL    REFERENCES users (id), --пользователь, создавший комментарий
    created     TIMESTAMP      NOT NULL    --дата создания комментария
);