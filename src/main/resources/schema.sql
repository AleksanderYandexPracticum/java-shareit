-- Таблица с запросами
CREATE TABLE IF NOT EXISTS requests (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, --уникальный идентификатор запроса
    description VARCHAR(10000)    NOT NULL,                         --текст запроса, содержащий описание требуемой вещи
    requestor_id BIGINT           NOT NULL REFERENCES users (id) ON DELETE CASCADE ON UPDATE CASCADE,   --пользователь, создавший запрос
    created     TIMESTAMP         NOT NULL                          --дата и время создания запроса
);

-- Таблица с вещами
CREATE TABLE IF NOT EXISTS items (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR(255)      NOT NULL CHECK (name <> ''),
    description VARCHAR(10000)    NOT NULL,  --если ставить TEXT не работает CONTANING при поиске
    is_available  boolean         NOT NULL,
    owner_id    BIGINT            REFERENCES users (id) ON DELETE CASCADE ON UPDATE CASCADE,
    request_id  BIGINT            REFERENCES requests (id)
);

